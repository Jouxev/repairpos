type TranslationKey = string

interface AppSettingsContextValue {
  settings: ShopSettings | null
  language: AppLanguage
  currency: AppCurrency
  currencySymbol: string
  direction: 'ltr' | 'rtl'
  locale: string
  isReady: boolean
  t: (key: TranslationKey, values?: TranslationValues) => string
  refresh: () => Promise<void>
}

const AppSettingsContext = createContext<AppSettingsContextValue | null>(null)

const interpolate = (template: string, values?: TranslationValues) => {
  if (!values) {
    return template
  }

  return Object.entries(values).reduce((result, [key, value]) => {
    return result.replace(new RegExp(`\\{${key}\\}`, 'g'), String(value))
  }, template)
}

export function AppSettingsProvider({ children }: { children: React.ReactNode }) {
  const [settings, setSettings] = useState<ShopSettings | null>(null)
  const [isReady, setIsReady] = useState(false)

  const refresh = useCallback(async () => {
    const nextSettings = await shopSettingsService.getSettings()
    setSettings(nextSettings)
  }, [])

  useEffect(() => {
    const loadSettings = async () => {
      try {
        await refresh()
      } finally {
        setIsReady(true)
      }
    }

    void loadSettings()
  }, [refresh])

  const language = normalizeLanguage(settings?.language)
  const currency = normalizeCurrency(settings?.currency)
  const currencySymbol = settings?.currencySymbol || getCurrencySymbol(currency)
  const languageOption = getLanguageOption(language)
  const direction = languageOption.direction
  const locale = languageOption.locale

  useEffect(() => {
    document.documentElement.lang = language
    document.documentElement.dir = direction
    document.body.dir = direction
  }, [direction, language])

  const t = useCallback(
    (key: TranslationKey, values?: TranslationValues) => {
      const languageMap = translations[language] as Record<string, string>
      const defaultMap = translations.en as Record<string, string>
      const template = languageMap[key] || defaultMap[key] || String(key)
      return interpolate(template, values)
    },
    [language],
  )

  const contextValue = useMemo<AppSettingsContextValue>(
    () => ({
      settings,
      language,
      currency,
      currencySymbol,
      direction,
      locale,
      isReady,
      t,
      refresh,
    }),
    [currency, currencySymbol, direction, isReady, language, locale, refresh, settings, t],
  )

  return <AppSettingsContext.Provider value={contextValue}>{children}</AppSettingsContext.Provider>
}

export const useAppSettings = () => {
  const context = useContext(AppSettingsContext)

  if (!context) {
    throw new Error('useAppSettings must be used within AppSettingsProvider')
  }

  return context
}

export const getTranslatedLanguageLabel = (language: AppLanguage, t: AppSettingsContextValue['t']) => {
  const labels: Record<AppLanguage, TranslationKey> = {
    en: 'english',
    fr: 'french',
    ar: 'arabic',
  }

  return t(labels[language])
}

export const getTranslatedCurrencyLabel = (currency: AppCurrency, t: AppSettingsContextValue['t']) => {
  const labels: Record<AppCurrency, TranslationKey> = {
    DZD: 'algerianDinar',
    EUR: 'euro',
    USD: 'usDollar',
  }

  const currencyOption = getCurrencyOption(currency)
  return `${currency} - ${t(labels[currency])} (${currencyOption.symbol})`
}
